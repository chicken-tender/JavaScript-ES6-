// 변수를 만드는 키워드는 let(ES6 부터 추가 됨), var이 있음.
//    가급적 let으로 쓸 것: 중복 선언 불가(설명은 아래 코드 참조)
// 상수를 만드는 키워드는 const(ES6 부터 추가 됨)
let num = 20 + 30;
console.log(num);
let name = "곰돌이사육사";
console.log(name);

// var를 쓰면 안되는 이유
var num1 = 10 + 100;
console.log(num1);
var num1 = 10 + 200;
console.log(num1);
